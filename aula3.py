# AULA SOBRE STRING E PRINT
'''
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
Funciona tanto entre aspas simples quanto em aspas duplas

Escape utiliza a 'barra invertida' para ignorar a próxima letra de uma string

Ao digitar 'r' antes do string na função print, é mostrado a letra do escape no terminal também.
'''

# Aspas simples
print('---Exemplos de print com aspas simples---')
print('Texto entre aspas simples')
print('colocar "aspas duplas" entre "aspas simples" também funciona')
print()

# Aspas duplas
print("---Exemplos de print com aspas duplas---")
print("Texto entre aspas duplas")
print("'aspas simples' dentro das aspas duplas")
print()

# Escape
print('---Exemplos de print utilizando o Escape---')
print('Utilizando o \"Escape\"')
print()

# r
print('---Exemplos de print utilizando o r---')
print(r'Utilizando o \"R\"')
print()