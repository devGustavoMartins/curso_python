# AULA SOBRE PRINT
'''
A função print é utilizada para exibir as coisas na tela/terminal.
Entre os parênteses, a função recebe um argumento.

sep é inserido entre os termos do print.
end é inserido no final do print.

\r\n -> CRLF -> Windows antigo
\n -> LF -> Windows atual, Linux e Mac
'''

print(12, 34, sep='.', end='.')
print(56, 78, sep='.')