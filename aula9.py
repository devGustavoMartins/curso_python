# AULA SOBRE OPERAÇÕES MATEMÁTICAS
adicao = 10 + 10
print('Adicao ', adicao)

subtracao = 10 - 10
print('Subtracao ', subtracao)

multiplicacao = 10 * 10
print('Multiplicacao ', multiplicacao)

divisao = 10 / 10  # Float.
print('Divisao ', divisao)

divisao_inteira = 10 // 3  # Float ou Int, Resultado da divisão por número inteiro, não dando o resto.
print('Divisao Inteira ', divisao_inteira)

exponenciacao = 2 ** 10
print('Exponenciacao ', exponenciacao)

modulo = 55 % 2  # Resto da divisão.
print('Modulo', modulo)

a = 22
if(a % 2 == 0):
    print(a, 'é par')
if(a % 2 != 0):
    print(a, 'é ímpar')