# AULA SOBRE BOOL
'''
Tipo de dado bool (boolean)
Serve para questionar algo em um programa
Só pode dar duas respostas: true ou false
Existem vários operadores para "questionar". Dentre eles o ==, que é um operador lógico que questiona se um valor é igual ao outro.
'''

print('Para valores iguais')
print(1 == 1)
print()

print('Para valores diferentes')
print(0 == 1)
print()