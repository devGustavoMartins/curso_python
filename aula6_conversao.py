'''
conversão de tipos, coerção, type convertion, typecasting, coercion é o ato de converter um tipo em outro.

tipos imutáveis e primitivos: str, int, float, bool.
'''

print(str(1) + str(2)) # Transforma em string
print(int('1') + int('2')) # Transforma em int
print(float('1.5') + float('2.5')) # Transforma em float
print(bool(' ')) # Transforma em bool, com algo nas aspas -> True
print(bool('')) # Transforma em bool, nada nas aspas -> False
